// Inline CSS (can also be an array of URLs)
const cssContent = [];

async function main() {
	console.log("PaperView PDF Export - Node.js Example");
	console.log("======================================\n");

	try {
		// Create output directory
		const outputDir = path.join(__dirname, "output");
		if (!fs.existsSync(outputDir)) {
			fs.mkdirSync(outputDir, { recursive: true });
		}

		const outputPath = path.join(outputDir, "sample-document.pdf");

		console.log("Generating PDF...");
		console.log(`Output: ${outputPath}\n`);

		// Generate PDF
		const pdfBuffer = await NodePDFExporter.fromHTML(htmlContent, cssContent, {
			format: "A4",
			printBackground: true,
			margin: {
				top: "2cm",
				right: "1.5cm",
				bottom: "2cm",
				left: "1.5cm"
			}
		});

		// Save PDF
		await NodePDFExporter.save(pdfBuffer, outputPath);

		console.log("PDF generated successfully!");
		console.log(`File size: ${(pdfBuffer.length / 1024).toFixed(2)} KB`);
		console.log(`Saved to: ${outputPath}`);
	} catch (error) {
		console.error("Error generating PDF:", error.message);
		process.exit(1);
	}
}

// Run the example
main();
